<template>
  <svg :class="svgClass" aria-hidden="true" v-on="$attrs">
    <use :xlink:href="iconName"/>
  </svg>
</template>
<script lang="ts">
import {Options,Vue} from "vue-class-component"
import {Prop} from "vue-property-decorator"
@Options({
    inheritAttrs:false
})
export default class SVGComponent extends Vue {
    @Prop({type:String,required:true})iconClass!:string
    @Prop({type:String,required:false})className?:string
    get iconName(){
      return `#icon-${this.iconClass}`
    }
    get svgClass(){
      if (this.className){
        return 'svg-icon' +this.className
      }else{
      return 'svg-icon'
      }
    }
}
</script>
<style scoped>
  .svg-icon {
    width: 1em;
    height: 1em;
    vertical-align:unset;
    fill: currentColor;
    overflow: hidden;
  }
</style>